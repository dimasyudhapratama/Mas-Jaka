package com.erait.mas_jaka.Model;

public class DaganganModel {
    private String id_dagangan, nama_dagangan, harga, stok, bagi_hasil, est_point, dagangan_image;

    public DaganganModel() {
    }

    public String getId_dagangan() {
        return id_dagangan;
    }

    public void setId_dagangan(String id_dagangan) {
        this.id_dagangan = id_dagangan;
    }

    public String getNama_dagangan() {
        return nama_dagangan;
    }

    public void setNama_dagangan(String nama_dagangan) {
        this.nama_dagangan = nama_dagangan;
    }

    public String getHarga() {
        return harga;
    }

    public void setHarga(String harga) {
        this.harga = harga;
    }

    public String getStok() {
        return stok;
    }

    public void setStok(String stok) {
        this.stok = stok;
    }

    public String getBagi_hasil() {
        return bagi_hasil;
    }

    public void setBagi_hasil(String bagi_hasil) {
        this.bagi_hasil = bagi_hasil;
    }

    public String getEst_point() {
        return est_point;
    }

    public void setEst_point(String est_point) {
        this.est_point = est_point;
    }

    public String getDagangan_image() {
        return dagangan_image;
    }

    public void setDagangan_image(String dagangan_image) {
        this.dagangan_image = dagangan_image;
    }
}
